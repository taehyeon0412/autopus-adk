// TestRenderParseRoundTrip_MultiBuild_PreservesBuilds verifies that a
// ScenarioSet with multi-build entries survives render → parse round-trip.
func TestRenderParseRoundTrip_MultiBuild_PreservesBuilds(t *testing.T) {
	t.Parallel()

	original := &ScenarioSet{
		ProjectName: "roundtrip",
		ProjectType: "CLI",
		Binary:      "auto",
		Builds: []BuildEntry{
			{Command: "go build ./cmd/auto/", Label: "ADK", SubmodulePath: "autopus-adk"},
			{Command: "npm run build", Label: "Frontend", SubmodulePath: "Autopus/frontend"},
		},
	}

	rendered, err := RenderScenarios(original)
	require.NoError(t, err)

	parsed, err := ParseScenarios(rendered)
	require.NoError(t, err)
	require.Len(t, parsed.Builds, 2)
	assert.Equal(t, "ADK", parsed.Builds[0].Label)
	assert.Equal(t, "go build ./cmd/auto/", parsed.Builds[0].Command)
	assert.Equal(t, "Frontend", parsed.Builds[1].Label)
	assert.Equal(t, "npm run build", parsed.Builds[1].Command)
}

