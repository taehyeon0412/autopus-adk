// TestParseScenarios_AllFields_Parsed verifies that every field in a scenario
// block is correctly deserialized into the Scenario struct.
func TestParseScenarios_AllFields_Parsed(t *testing.T) {
	t.Parallel()

	input := `# E2E Scenarios — myproject

> Auto-generated by ` + "`/auto setup`" + `. Updated by ` + "`/auto sync`" + `.

## Project Type: CLI
## Binary: mybinary
## Build: go build -o mybinary .

### S3: full-fields — All fields present
- **Command**: ` + "`mybinary init --name test`" + `
- **Precondition**: binary built, config exists
- **Env**: MY_KEY=myval
- **Expect**: exit 0
- **Verify**: exit_code(0), stdout_contains("initialized")
- **Depends**: S1
- **Status**: active
`

	set, err := ParseScenarios([]byte(input))
	require.NoError(t, err)
	require.Len(t, set.Scenarios, 1)

	s := set.Scenarios[0]
	assert.Equal(t, 3, s.Number)
	assert.Equal(t, "full-fields", s.ID)
	assert.Equal(t, "All fields present", s.Description)
	assert.Contains(t, s.Command, "mybinary init")
	assert.Equal(t, "binary built, config exists", s.Precondition)
	assert.Equal(t, "MY_KEY=myval", s.Env)
	assert.Equal(t, "exit 0", s.Expect)
	assert.Len(t, s.Verify, 2)
	assert.Equal(t, "S1", s.Depends)
	assert.Equal(t, "active", s.Status)
}

