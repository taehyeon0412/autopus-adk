// TestParseScenarios_MultiBuildLine_PopulatesBuilds verifies that a multi-build
// Build line is parsed into both the raw Build string and the Builds slice.
func TestParseScenarios_MultiBuildLine_PopulatesBuilds(t *testing.T) {
	t.Parallel()

	input := `# E2E Scenarios — multiproject

> Auto-generated by ` + "`/auto setup`" + `. Updated by ` + "`/auto sync`" + `.

## Project Type: CLI
## Binary: auto
## Build: go build ./cmd/auto/ (ADK), go build ./cmd/server/ (Backend)
`

	set, err := ParseScenarios([]byte(input))
	require.NoError(t, err)
	assert.Equal(t, "go build ./cmd/auto/ (ADK), go build ./cmd/server/ (Backend)", set.Build)
	require.Len(t, set.Builds, 2)
	assert.Equal(t, "ADK", set.Builds[0].Label)
	assert.Equal(t, "go build ./cmd/auto/", set.Builds[0].Command)
	assert.Equal(t, "autopus-adk", set.Builds[0].SubmodulePath)
	assert.Equal(t, "Backend", set.Builds[1].Label)
	assert.Equal(t, "go build ./cmd/server/", set.Builds[1].Command)
}

