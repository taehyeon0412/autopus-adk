// TestTestRunCmd_DeprecatedScenarios_Skipped verifies that deprecated scenarios
// are not executed (only active ones count).
func TestTestRunCmd_DeprecatedScenarios_Skipped(t *testing.T) {
	t.Parallel()

	// Given: scenarios.md with one active and one deprecated scenario
	dir := t.TempDir()
	writeScenariosFile(t, dir, scenariosWithDeprecated)

	// When: "auto test run" is executed
	cmd := newAutoTestCmd()
	var out bytes.Buffer
	cmd.SetOut(&out)
	cmd.SetArgs([]string{"run", "--project-dir", dir})
	err := cmd.Execute()

	// Then: command succeeds (deprecated scenario not counted as failure)
	require.NoError(t, err)
	// Summary line should show 1 run (not 2)
	assert.Contains(t, out.String(), "1/1")
}

